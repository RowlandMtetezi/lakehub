					Sat May 4th 2013, 9am - 1pm, Duke of Breeze
					<h5>LakeHub Kisumu kicks off with Tech Workshop #0!</h5>
				</p>
				<p>
					This weekend LakeHub hosted its first formal event in the heart of Western Kenya - Kisumu City! A crew of about 25 techies - students, young professionals, and business people gathered for a half-day tech-workshop at the Duke of Breeze Hotel.
					The group broke out into parallel sessions that covered: 
					<ol>
						<li>Intro to Thinking like a Programmer</li>
						<li>Web and Mobile App Development</li>
					</ol>
					The mobile app development session was led by 3rd year Maseno 
					University computer science student James Odede, president of the University's ICT-Guild. 
					Building on Ahmed Misbah's powerpoint overview - James 
					guided an interactive session in Eclipse demonstrating the first steps 
					to create a mock-application called "mFish" and talked through the idea of how Kisumu's 
					programmers could serve local needs with locally focused applications.
					<p>
						Then the crew re-assembled for a design thinking exercise led by Maria Langat. 
						Splitting into clusters of two and three and following the format of the Stanford 
						Design School's <a href="https://dschool.stanford.edu/groups/k12/wiki/c739e/">Wallet Project</a> 
						the teams designed wallets to suite the users needs of security, fashion, convenience, space, and variety.
					</p>
