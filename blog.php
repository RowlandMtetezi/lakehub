					Sun May 12th 2013, Duke Of Breeze Rooftop
					<h5>Coding, Hacking &amp; Hanging Out </h5>
				</p>
				<p>
					On Sunday May 12th, LakeHub set up on the rooftop of the Duke of Breeze in Kisumu and welcomed softeware programmers 
					and hardware hackers for trainings, videos, and collaboration. 
					Simeon Obwogo led a team through the Android software development kit (SDK) exploring the initial components of creating an app.
					At the same time Peter Mbari of Access:Energy brought an <a href="http://en.wikipedia.org/wiki/Arduino" />Arduino</a>
                    microcontroller as well as his own independently constructed microcontroller and demonstrated basic principles of hardware-based programs. 
					He showed a few different software programs that sync with microcontrollers and allow for simulations of the actual hardware within the computer. 
				</p>
				<p>
					The informal event attracted individuals interested in a wide range of programming activities. 
					The event began at 1 PM and carried on until sunset with the attendees watching videos on how to:
					<ol>
						<li>Construct jet engines</li>
						<li>Build simple websites</li>
						<li>Setting microcontroller to cause an LED to blink at varying rates</li>
					</ol>
					<a href="http://www.youtube.com/watch?v=Jqfaayoqgxo&feature=youtu.be" />Reference</a>
					This was LakeHub's first hangout, and they will continue to happen twice each month on Saturday's from 1 to 5 PM. 
					Attendees at the hangout are welcome to focus on topics ranging from:
						<ol>
							<li>Game Design</li>
							<li>Mobile Application Development</li>
							<li>Electronic Circuits</li>
						</ol>
				</p>
