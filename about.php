					<h5>What Is Lakehub?</h5>
					Many of you might be asking what is LakeHub? Essentially, LakeHub is the idea that Kenya has more to offer the global tech community than what can be found in Nairobi. What iHub, 88mph, Nailab etc. have done in Nairobi is incredible, but our thought is that Western Kenya has a considerable amount of talent, interest, and ability to meaningfully contribute in the tech-space and to build tech-oriented businesses that will be to Kenya's benefit. The thought is that there is enough interest and momentum towards technology at the moment that creating a space, support system, and community that fosters entrepreneurship and technology now will pay-off and further build interest and ability in the region over the coming years.
